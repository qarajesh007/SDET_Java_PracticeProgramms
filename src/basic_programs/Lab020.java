package basic_programs;

public class Lab020
{
    public static void main(String[] args) {
        byte b=100; // 1 byte 8bits
        short s=2342; // 2byte 16bits
        int i=686548; //4byte 32bits
        long l=986453678L; //9byte 64bits

        char c='a'; // 2byte 16bits

        float f=3.14f; //4byte 32bits
        double d=6243.6242724; //8byte 64bits

        System.out.println("The value of b="+ b);
        System.out.println("The value of s="+ s);
        System.out.println("The value of i="+ i);
        System.out.println("The value of l"+ l);
        System.out.println("The value of c"+ c);
        System.out.println("The value of f"+ f);
        System.out.println("The value of d"+ d);

    }
}
