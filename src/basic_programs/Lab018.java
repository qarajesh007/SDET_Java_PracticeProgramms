package basic_programs;

public class Lab018
{
    public static void main(String[] args)
    {  //primitive datatypes
        //Numeric -Integer
     byte b=127; // size= 1byte                    range -128 to 127
        short s=128; // size= 2byte                range -32768 to 32767
        int i=1234; // size= 4byte                   range -2^31 to 2^31 -1
        long l=9268336282868L;   // size= 8byte     range -2^63 to 2^63 -1

        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);

    }
}
